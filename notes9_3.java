class notes9_3 { 
	// main methods require the public
	// key notation to allow access from outside
	// the module
	// static means that the method runs the same
	// way every time it is accessed
	// void is the return value
	// nothing (void) is returned
	// main is the name of the method
	// String[] args is the parameter
	// {} define the area for the main method
	public static void main(String[] args) {
		// this is inside (defining) the main
		// method
		System.out.print("Hello World!");
	}

}